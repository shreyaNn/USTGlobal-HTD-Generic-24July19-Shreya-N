class Logical_Operators{
	public static void main(String[] args) {
		int a=10,b=20,c=30;
		//System.out.println(a>b);
		// Logical AND
				System.out.println("AND");

		System.out.println(a<b && a<c); 
				System.out.println(a<b && a>c);
		System.out.println(a>b && a<c);                                                                 
System.out.println(a>b && a>c);  
// Logical OR
		System.out.println("OR");

		System.out.println(a<b || a<c);
				System.out.println(a<b || a>c);
		System.out.println(a>b || a<c);                                                                 
System.out.println(a>b || a>c);  

	}
}                              